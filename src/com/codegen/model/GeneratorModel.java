package com.codegen.model;

public class GeneratorModel extends JavaModel {

	private String changeClass;
	private String sysDate;
	private String lowChangeClass;

	public String getChangeClass() {
		return changeClass;
	}

	public void setChangeClass(String changeClass) {
		this.changeClass = changeClass;
	}

	public String getSysDate() {
		return sysDate;
	}

	public void setSysDate(String sysDate) {
		this.sysDate = sysDate;
	}

	public String getLowChangeClass() {
		return lowChangeClass;
	}

	public void setLowChangeClass(String lowChangeClass) {
		this.lowChangeClass = lowChangeClass;
	}

}
